	void createTextureImage() {
		//首先会创建一个暂存资源，然后使用像素数据填充它，接着将像素数据从暂存资源复制到用来渲染的图像对象
		//实际上也可以直接创建暂存图像来用于渲染，Vulkan允许直接从VkBuffer中复制像素数据到图像对象		
		
		//查询内存需求，分配并绑定设备内存

		//图像的布局会影响它的像素数据在内存中的组织方式
		//一般而言，对于现在的硬件设备，直接将像素数据按行进行存储并不能得到最佳的性能表现。
		//不同的图像布局对图像操作也有一定的影响
		//图像布局类型
		//VK_IMAGE_LAYOUT_PRESENT_SRC_KHR：适合呈现操作
		//VK_IMAGE_LAYOUT_COLOR_ATTACHMENT_OPTIMAL：适合作为颜色附着，在片段着色器中写入颜色数据。
		//VK_IMAGE_LAYOUT_TRANSFER_SRC_OPTIMAL：适合作为传输操作的数据来源，比如vkCmdCopyImageToBuffer
		//VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL：适合作为传输操作的目的位置，比如vkCmdCopyBufferToImage
		//VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL：适合在着色器中进行采样操作

		//最常用的变换图像布局的方式是使用管线屏障(pipeline barrier)
		//主要被用来同步资源访问，比如保证图像在被读取之前数据被写入。它也可以被用来变换图像布局
		//如果队列的所有模式为VK_SHARING_MODE_EXCLUSIVE，管线屏障(pipeline barrier)还可以被用来传递队列所有权



		//加载图像数据到一个Vulkan图像对象
		Texture* tex = new Texture("assets/textures/texture.jpg");
		
		//暂存缓冲，创建一个CPU可见的缓冲，调用vkMapMemory函数映射内存，将图像像素数据复制到其中
		//使用的缓冲内存需要对CPU可见
		VkBuffer stagingBuffer;
		VkDeviceMemory stagingBufferMemory;
		createBuffer(tex->getImageSize(), VK_BUFFER_USAGE_TRANSFER_SRC_BIT,
			VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, 
			stagingBuffer, stagingBufferMemory);
		//映射内存，将图像数据复制到缓冲中
		void* data;
		vkMapMemory(device, stagingBufferMemory, 0, tex->getImageSize(), 0, &data);
		memcpy(data, tex->getPixels(), static_cast<size_t>(tex->getImageSize()));
		vkUnmapMemory(device, stagingBufferMemory);

		tex->destroy();
		
		createImage(tex->getWidth(), tex->getHeight(), VK_FORMAT_R8G8B8A8_SRGB, VK_IMAGE_TILING_OPTIMAL, VK_IMAGE_USAGE_TRANSFER_DST_BIT | VK_IMAGE_USAGE_SAMPLED_BIT, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, textureImage, textureImageMemory);

		//复制暂存缓冲中的数据到纹理图像，需要进行下面两步操作：
		//变换纹理图像到VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL
		//执行图像数据复制操作
		transitionImageLayout(textureImage, VK_FORMAT_R8G8B8A8_SRGB, VK_IMAGE_LAYOUT_UNDEFINED, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL);
		copyBufferToImage(stagingBuffer, textureImage, static_cast<uint32_t>(tex->getWidth()), static_cast<uint32_t>(tex->getHeight()));
		//创建的图像对象使用VK_IMAGE_LAYOUT_UNDEFINED布局，所以转换图像布局时应该将VK_IMAGE_LAYOUT_UNDEFINED指定为旧布局
		
		//为了能够在着色器中采样纹理图像数据，还需要进行一次图像布局变换
		transitionImageLayout(textureImage, VK_FORMAT_R8G8B8A8_SRGB, VK_IMAGE_LAYOUT_TRANSFER_DST_OPTIMAL, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL);

		delete tex;

		vkDestroyBuffer(device, stagingBuffer, nullptr);
		vkFreeMemory(device, stagingBufferMemory, nullptr);
	}

